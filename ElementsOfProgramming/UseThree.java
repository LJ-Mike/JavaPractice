/**
 * Receive 3 names and print in reverse order
 */

public class UseThree {
    public static void main(String[] args) {
        System.out.print("Hi ");
        System.out.print(args[2] + ", ");
        System.out.print(args[1] + ", and ");
        System.out.print(args[0] + ".");
        System.out.println();
    }
}
